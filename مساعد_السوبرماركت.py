import json
from datetime import datetime
from typing import Dict, List, Optional

class سلة_التسوق:
    """سلة التسوق الافتراضية للعميل"""
    def __init__(self):
        self.المنتجات = []
        self.حالة_الطلب = "جديد"  # جديد, قيد_التحضير, مؤكد, ملغي
        self.اجمالي_تقديري = 0
    
    def أضف_منتج(self, اسم: str, الكمية: float, السعر: float = None):
        """إضافة منتج للسلة مع تحديث الإجمالي"""
        # تحديد الوحدة المناسبة
        وحدة = "كيلو"
        if any(word in اسم for word in ["صلصة", "كاتشب", "زيت"]):
            وحدة = "عبوة"
        elif any(word in اسم for word in ["صابون", "مناديل", "فليكس"]):
            وحدة = "قطعة"
        
        # أسعار تقديرية (للعرض فقط)
        الاسعار_التقديرية = {
            "رز الضحى": 32,
            "عدس أصفر": 28,
            "صلصة هاينز": 18,
            "زيت ذهبي": 45,
            "سكر": 25,
            "مكرونة": 15
        }
        
        السعر_التقديري = الاسعار_التقديرية.get(اسم, 0)
        
        self.المنتجات.append({
            "اسم": اسم,
            "كمية": الكمية,
            "وحدة": وحدة,
            "سعر_تقديري": السعر_التقديري,
            "اجمالي": السعر_التقديري * الكمية if السعر_التقديري else 0
        })
        
        self.اجمالي_تقديري = sum(p["اجمالي"] for p in self.المنتجات)
        
    def اعرض_السلة(self) -> str:
        """عرض محتويات السلة بشكل مرتب"""
        if not self.المنتجات:
            return "🛒 السلة فاضية حالياً"
        
        نص = "🛒 **سلة التسوق الخاصة بك:**\n"
        نص += "─" * 40 + "\n"
        
        for i, منتج in enumerate(self.المنتجات, 1):
            سعر = f"{منتج['سعر_تقديري']} جنيه" if منتج['سعر_تقديري'] else "سعر حسب الوزن"
            نص += f"{i}. {منتج['اسم']}\n"
            نص += f"   • الكمية: {منتج['كمية']} {منتج['وحدة']}\n"
            نص += f"   • السعر: {سعر}\n"
        
        نص += "─" * 40 + "\n"
        نص += f"💰 **الإجمالي التقديري: {self.اجمالي_تقديري} جنيه**\n"
        نص += f"📦 حالة الطلب: {self.حالة_الطلب}\n"
        
        return نص
    
    def تأكيد_الطلب(self):
        """تأكيد الطلب وإنهاءه"""
        self.حالة_الطلب = "مؤكد"
        return f"✅ تم تأكيد طلبك بنجاح! الإجمالي: {self.اجمالي_تقديري} جنيه. شكراً لتسوقك معنا."

class مساعد_المبيعات:
    """المساعد الذكي للمبيعات - بيع منحاز واحترافي"""
    
    def __init__(self):
        self.سلة = سلة_التسوق()
        self.اسم_العميل = ""
        self.سياق_المحادثة = []  # حفظ تاريخ المحادثة
        self.المنتجات_المقترحة = {
            "اكله": {
                "الرئيسية": ["رز الضحى", "عدس أصفر", "صلصة هاينز", "مكرونة قلم"],
                "البدائل": ["رز المصريين", "عدس بني", "صلصة كاليفورنيا", "مكرونة قرون"]
            },
            "مشروبات": {
                "الرئيسية": ["عصير قمر", "مياه معدنية", "بيبسي"],
                "البدائل": ["عصير هاي", "مياه أكوا", "كوكاكولا"]
            },
            "منظفات": {
                "الرئيسية": ["صابون بريل", "فليكس اتوماتيك", "كلوركس"],
                "البدائل": ["صابون اريال", "فليكس عادي", "ديتول"]
            }
        }
    
    def حفظ_الرسالة(self, دور: str, محتوى: str):
        """حفظ الرسالة في سجل المحادثة"""
        self.سياق_المحادثة.append({
            "دور": دور,  # "عميل" أو "مساعد"
            "محتوى": محتوى,
            "وقت": datetime.now().strftime("%H:%M")
        })
    
    def استقبال_العميل(self) -> str:
        """ترحيب احترافي بالعميل"""
        التحية = """🌟 أهلاً وسهلاً بك في **سوبر ماركت أونلاين**! 🌟

أنا مساعدك الشخصي للتسوق. هساعدك تختار أفضل المنتجات وأضيفها لسلة مشترياتك مباشرة.

📝 **إيه اللي هتحتاجه النهاردة؟**
- عاوز تجهز أكل معين (كشري، مكركة، طبيخ)؟
- ولا عاوز مشتريات أسبوعية؟

أنا معاك خطوة بخطوة، وهرشحلك أفضل الأنواع المتوفرة."""
        
        self.حفظ_الرسالة("مساعد", التحية)
        return التحية
    
    def تحليل_طلب_العميل(self, رسالة: str) -> Dict:
        """تحليل طلب العميل وفهم احتياجاته"""
        رسالة = رسالة.lower()
        
        # فهم نوع الطلب
        تحليل = {
            "نوع_الطلب": "عام",
            "المنتجات_المذكورة": [],
            "الكمية": 1,
            "التصنيف": "اكله"
        }
        
        # البحث عن كميات
        if "كيلو" in رسالة:
            try:
                الكمية = float(''.join(filter(str.isdigit, رسالة.split("كيلو")[0])))
                تحليل["الكمية"] = الكمية
            except:
                pass
        
        # تصنيف الطلب
        if any(word in رسالة for word in ["كشري", "رز", "عدس", "مكرونة", "صلصة"]):
            تحليل["التصنيف"] = "اكله"
            تحليل["نوع_الطلب"] = "كشري" if "كشري" in رسالة else "اكل عام"
        
        elif any(word in رسالة for word in ["عصير", "مشروب", "مياه", "بيبسي"]):
            تحليل["التصنيف"] = "مشروبات"
            تحليل["نوع_الطلب"] = "مشروبات"
        
        elif any(word in رسالة for word in ["صابون", "منظف", "فليكس"]):
            تحليل["التصنيف"] = "منظفات"
            تحليل["نوع_الطلب"] = "منظفات"
        
        # البحث عن منتجات مذكورة
        for تصنيف, بيانات in self.المنتجات_المقترحة.items():
            for منتج in بيانات["الرئيسية"] + بيانات["البدائل"]:
                if منتج.lower() in رسالة:
                    تحليل["المنتجات_المذكورة"].append(منتج)
        
        return تحليل
    
    def اقتراح_ذكي(self, تحليل: Dict) -> str:
        """تقديم اقتراحات ذكية ومنحازة بناءً على تحليل الطلب"""
        
        if تحليل["التصنيف"] == "اكله":
            if "كشري" in تحليل["نوع_الطلب"] or any(p in ["عدس", "رز"] for p in تحليل["المنتجات_المذكورة"]):
                return self.اقتراح_كشري(تحليل["الكمية"])
            else:
                return self.اقتراح_اكل_عام(تحليل["كمية"])
        
        elif تحليل["التصنيف"] == "مشروبات":
            return self.اقتراح_مشروبات()
        
        elif تحليل["التصنيف"] == "منظفات":
            return self.اقتراح_منظفات()
        
        else:
            return self.اقتراح_عام()
    
    def اقتراح_كشري(self, الكمية: float = 1) -> str:
        """اقتراح متكامل للكشري مع توسيع السلة بذكاء"""
        
        # حساب الكميات المناسبة
        كمية_الرز = الكمية
        كمية_العدس = الكمية * 0.7
        كمية_الصلصة = max(1, round(الكمية / 2))
        
        # إضافة المنتجات للسلة
        self.سلة.أضف_منتج("رز الضحى", كمية_الرز)
        self.سلة.أضف_منتج("عدس أصفر", كمية_العدس)
        self.سلة.أضف_منتج("صلصة هاينز", كمية_الصلصة)
        
        الاقتراح = f"""🍛 **اقتراح متكامل للكشري المصري:**

بناءً على طلبك، عملتلك package متكامل لألذ كشري:

• **رز الضحى المصري** - {كمية_الرز} كيلو (أفضل رز للكشري، حبة قصيرة ونضيفة)
• **عدس أصفر مستورد** - {كمية_العدس} كيلو (ينضج بسرعة وطعمه ممتاز)
• **صلصة هاينز** - {كمية_الصلصة} عبوة (السر في طعم الكشري الأصلي)

✨ **كمان ممكن تحتاج:**
- مكرونة قلم صغير (للإضافات)
- دقيق (لتحضير الدقة)
- زيت ذهبي للقلي

📦 **حطيتلك الأساسيات في السلة.** تقدر تراجعها وتأكد الطلب!"""
        
        self.حفظ_الرسالة("مساعد", الاقتراح)
        return الاقتراح
    
    def اقتراح_اكل_عام(self, الكمية: float = 1) -> str:
        """اقتراح لأكل عام"""
        self.سلة.أضف_منتج("رز الضحى", الكمية)
        self.سلة.أضف_منتج("زيت ذهبي", 1)
        
        الاقتراح = f"""🍳 **اقتراح طبخ منزلي:**

• **رز الضحى** - {الكمية} كيلو (مصري فاخر)
• **زيت ذهبي** - 1 عبوة (للطهي والقلي)

💡 **نصيحتي:** لو بتعملي طبيخ، محتاجة كمان:
- مكرونة (لو عاوزة مكرونة بالباشميل)
- صلصة (للأكلات المصرية)
- بهارات (حسب الأكلة)

أضفتلك الأساسيات، عاوز أضيف حاجة تانية؟"""
        
        self.حفظ_الرسالة("مساعد", الاقتراح)
        return الاقتراح
    
    def اقتراح_مشروبات(self) -> str:
        """اقتراح مشروبات"""
        self.سلة.أضف_منتج("عصير قمر", 2)
        self.سلة.أضف_منتج("مياه معدنية", 6)
        
        return """🥤 **تشكيلة المشروبات:**

أحضرتلك أقوى العروض:
• **عصير قمر** - 2 عبوة (طبيعي 100%)
• **مياه معدنية** - 6 قارورة (نقية)

كمان عندنا:
- بيبسي (علب أو قوارير)
- عصير هاي (مانجو/برتقال)

هل تحب أضيف حاجة من دول؟"""
    
    def اقتراح_منظفات(self) -> str:
        """اقتراح منظفات"""
        self.سلة.أضف_منتج("صابون بريل", 2)
        self.سلة.أضف_منتج("فليكس اتوماتيك", 1)
        
        return """🧼 **منظفات منزلية:**

اخترتلك الأفضل:
• **صابون بريل** - 2 قطعة (للأواني، بيساعد على إزالة الدهون)
• **فليكس اتوماتيك** - 1 عبوة (للغسالة، رائحة منعشة)

لو عاوز حاجة للتعقيم، في كلوركس وديتول كمان."""
    
    def اقتراح_عام(self) -> str:
        """اقتراح عام"""
        return """🛍️ **اقتراحات اليوم:**

أشهر المنتجات اللي الناس بتطلبها:
• رز الضحى المصري
• عدس أصفر مستورد
• صلصة هاينز
• زيت ذهبي للقلي

إيه رأيك نبدأ بحاجة من دول؟"""
    
    def رد_على_العميل(self, رسالة: str) -> str:
        """الرد الرئيسي على العميل - المنطق الذكي"""
        
        # حفظ رسالة العميل
        self.حفظ_الرسالة("عميل", رسالة)
        
        # تحليل الطلب
        تحليل = self.تحليل_طلب_العميل(رسالة)
        
        # حالات الرد المختلفة
        if any(word in رسالة.lower() for word in ["تمام", "ok", "ماشي", "حلو"]):
            return "👍 جميل! الطلب في السلة. عاوز تأكده ولا محتاج حاجة تانية؟"
        
        elif "عرض السلة" in رسالة or "السلة" in رسالة:
            return self.سلة.اعرض_السلة()
        
        elif "تأكيد" in رسالة or "أكد" in رسالة or "تمام كده" in رسالة:
            return self.سلة.تأكيد_الطلب()
        
        elif "شكرا" in رسالة or "متشكر" in رسالة:
            return "العفو على إيه! دايماً في خدمتك. لو احتجت حاجة تانية، أنا هنا 🤝"
        
        elif "مسح" in رسالة or "حذف" in رسالة:
            self.سلة = سلة_التسوق()
            return "🗑️ تم إفراغ السلة. تقدر تبدأ طلب جديد."
        
        else:
            # اقتراح ذكي بناءً على الطلب
            return self.اقتراح_ذكي(تحليل)
    
    def عرض_المحادثة(self) -> str:
        """عرض تاريخ المحادثة"""
        if not self.سياق_المحادثة:
            return "لا توجد محادثات سابقة"
        
        نص = "📜 **سجل المحادثة:**\n"
        نص += "─" * 40 + "\n"
        
        for رسالة in self.سياق_المحادثة:
            رمز = "👤" if رسالة["دور"] == "عميل" else "🤵"
            نص += f"{رمز} {رسالة['دور']} ({رسالة['وقت']}):\n"
            نص += f"   {رسالة['محتوى'][:50]}...\n"
        
        return نص


# ========== تشغيل المساعد ==========
if __name__ == "__main__":
    
    def تشغيل_المساعد():
        """تشغيل المساعد في وضع المحادثة"""
        
        المساعد = مساعد_المبيعات()
        
        print("=" * 60)
        print("🛒 مساعد التسوق الذكي - سوبر ماركت أونلاين")
        print("=" * 60)
        print()
        
        # استقبال العميل
        print(المساعد.استقبال_العميل())
        print()
        
        while True:
            try:
                # أخذ رسالة العميل
                رسالة = input("\n👤 أنت: ").strip()
                
                if رسالة.lower() in ["خروج", "exit", "quit", "bye"]:
                    print("\n🤵 مساعد: شكراً لتسوقك معنا! مع السلامة 👋")
                    break
                
                if رسالة.lower() in ["history", "سجل"]:
                    print("\n" + المساعد.عرض_المحادثة())
                    continue
                
                if not رسالة:
                    continue
                
                # رد المساعد
                رد = المساعد.رد_على_العميل(رسالة)
                print(f"\n🤵 مساعد:\n{رد}")
                
                # عرض السلة تلقائياً بعد أي إضافة
                if "سلة" not in رسالة and len(المساعد.سلة.المنتجات) > 0:
                    print(f"\n{المساعد.سلة.اعرض_السلة()}")
                
            except KeyboardInterrupt:
                print("\n\n👋 تم إنهاء الجلسة. مع السلامة!")
                break
            except Exception as e:
                print(f"\n⚠️ حدث خطأ: {e}")
    
    # تشغيل المساعد
    تشغيل_المساعد()
